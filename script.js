let yellow = new Set();
let black = new Set();
let yellow_index = {
    0: [],
    1: [],
    2: [],
    3: [],
    4: [],
};

function getSuggestions(res, guess, eval, suggestions) {
    guess = guess.toUpperCase();
    eval.forEach((e, idx) => {
        if (e === "correct") {
            res[idx] = guess[idx];
        } else if (e === "present") {
            yellow.add(guess[idx]);
            yellow_index[idx].push(guess[idx]);
        } else {
            black.add(guess[idx]);
        }
    });
    const temp = new Set(suggestions);
    temp.forEach((word) => {
        let flag = true;
        black.forEach((letter) => {
            if (word.indexOf(letter) > -1 && flag) {
                suggestions.delete(word);
                flag = false;
            }
        });
        if (flag) {
            yellow.forEach((letter) => {
                if (word.indexOf(letter) < 0 && flag) {
                    suggestions.delete(word);
                    flag = false;
                }
            });
        }
        if (flag) {
            res.forEach((letter, idx) => {
                if (letter != 0 && flag) {
                    if (letter != word[idx] || yellow_index[idx].indexOf(letter) > 0) {
                        suggestions.delete(word);
                        flag = false;
                    }
                }
            });
        }
    });
    return res, suggestions;
}

async function getHints() {
    const rows = document
        .querySelector("game-app")
        .shadowRoot.querySelectorAll("game-row");
    let res = [0, 0, 0, 0, 0];

    for (let i = 0; i < rows.length; i++) {
        if (rows[i].getAttribute("letters")) {
            const guess = rows[i].getAttribute("letters");
            const tiles = rows[i].shadowRoot.querySelectorAll("game-tile");
            eval = [];
            for (let j = 0; j < tiles.length; j++) {
                if (tiles[j].hasAttribute("evaluation")) {
                    eval.push(tiles[j].getAttribute("evaluation"));
                }
            }
            if (eval.length !== 0)
                res,
                (suggestions = await getSuggestions(res, guess, eval, suggestions));
        } else {
            console.log("empty");
        }
    }
    return suggestions;
}

function getAnswer() {
  let answer;
  try {
    answer = JSON.parse(
      localStorage.getItem("gameState")
    ).solution.toUpperCase();
  } catch (err) {
    answer = "Copying will not help you grow :)";
  }
  return answer;
}

function getUnlockStatus() {
  try {
    return localStorage.getItem("hasPlayerUnlockedAnswer");
  } catch (err) {
    return false;
  }
}

function setUnlockAnswer() {
  try {
    localStorage.setItem("hasPlayerUnlockedAnswer", true);
  } catch (err) {}
}

chrome.runtime.onMessage.addListener((request, sender, sendResponse) => {
    switch(request.message) {
      case "get_answer":
        sendResponse(getAnswer());
        break;
      
      case "get_unlock_status":
        sendResponse(getUnlockStatus());
        break;

      case "set_unlock_answer":
        setUnlockAnswer();
        break;

      case "get_suggestions":
        getHints().then((suggestions) => {
          let payload = Array.from(suggestions);
          console.log(payload.length);
          sendResponse(payload);
        });
        break;
    }
    return true;
});